import type { OutlineResult, DraftResult, ResearchResult } from '../types.js';

export interface TextGenerateInput {
  type: 'outline' | 'draft';
  topic: string;
  research?: ResearchResult;
  outline?: OutlineResult;
  language?: string;
  targetLength?: number;
}

export interface TextGenerateCapability {
  generate(input: TextGenerateInput): Promise<OutlineResult | DraftResult>;
}

/**
 * Creates a text generation capability
 * Stub: returns mock generated content
 */
export function createTextGenerateCapability(): TextGenerateCapability {
  return {
    async generate(input: TextGenerateInput): Promise<OutlineResult | DraftResult> {
      console.log(`[TextGenerateCapability] Generating ${input.type} for: ${input.topic}`);
      
      if (input.type === 'outline') {
        return generateOutline(input);
      } else {
        return generateDraft(input);
      }
    },
  };
}

function generateOutline(input: TextGenerateInput): OutlineResult {
  const { topic, research } = input;
  
  return {
    title: `${topic}: A Comprehensive Guide`,
    sections: [
      {
        heading: 'Introduction',
        subheadings: [
          `What is ${topic}?`,
          `Why ${topic} matters in 2024`,
          `Key benefits of understanding ${topic}`,
        ],
        keywords: [topic.toLowerCase(), 'introduction', 'overview'],
      },
      {
        heading: 'Understanding the Basics',
        subheadings: [
          'Core concepts explained',
          'How it works in practice',
          'Common misconceptions',
        ],
        keywords: research?.keyPoints.slice(0, 3) || ['basics', 'concepts', 'fundamentals'],
      },
      {
        heading: 'Key Benefits and Applications',
        subheadings: [
          'Primary advantages',
          'Real-world use cases',
          'Industry applications',
        ],
        keywords: ['benefits', 'advantages', 'applications', 'use-cases'],
      },
      {
        heading: 'Implementation Strategies',
        subheadings: [
          'Getting started guide',
          'Best practices',
          'Common pitfalls to avoid',
        ],
        keywords: ['implementation', 'strategy', 'guide', 'best-practices'],
      },
      {
        heading: 'Conclusion',
        subheadings: [
          'Summary of key points',
          'Future outlook',
          'Call to action',
        ],
        keywords: ['conclusion', 'summary', 'future', 'action'],
      },
    ],
    estimatedLength: 1500,
  };
}

function generateDraft(input: TextGenerateInput): DraftResult {
  const { topic, outline } = input;
  
  const content = outline
    ? generateFromOutline(topic, outline)
    : generateFallbackDraft(topic);
  
  return {
    content,
    wordCount: content.split(/\s+/).length,
    sections: outline?.sections.map(s => s.heading) || ['Introduction', 'Main Content', 'Conclusion'],
  };
}

function generateFromOutline(topic: string, outline: OutlineResult): string {
  const sections = outline.sections.map(section => {
    const paragraphs = section.subheadings.map(sh => {
      return `### ${sh}\n\nThis section explores ${sh.toLowerCase()} in detail, providing practical insights and actionable information for readers interested in ${topic}.`;
    }).join('\n\n');
    
    return `## ${section.heading}\n\n${paragraphs}`;
  }).join('\n\n');
  
  return `# ${outline.title}

*Generated with MMOS Blog Generation*

---

${sections}

---

## About This Article

This article was automatically generated by MMOS AI to provide comprehensive information about **${topic}**.

*Tags: ${outline.sections.map(s => s.heading.toLowerCase()).join(', ')}*`;
}

